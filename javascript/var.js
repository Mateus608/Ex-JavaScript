// declaration
var name;

// assignment (atribuição de valores)
name = 'Mateus';

// tipo de dado da variavel
console.log(typeof name);

// agrupamento de declarações
let age, isHuman;

age = 18;
isHuman = true;

// multiplos argumentos na funcao
console.log(name, age, isHuman);

// escrita de texto + variaveis
console.log('O ' + name + ' tem ' + age + ' anos.');

// interpolando valores com template literals or tamplate strings
console.log(`O ${name} tem ${age} anos`);